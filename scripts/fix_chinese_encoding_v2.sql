-- =====================================================
-- 分析乱码数据（供参考）
-- =====================================================
-- 乱码 "周超" HEX: C3A5E28098C2A8C3A8C2B6E280A6
-- 正确 "周超" UTF-8: E591A8E8B685

-- =====================================================
-- 方案1：使用Python生成修复SQL
-- =====================================================
-- 由于MySQL不支持直接的多重字符集转换，我们需要：
-- 1. 找出所有乱码数据
-- 2. 手动映射并更新正确值

-- 临时方法：直接用HEX值匹配后更新为正确值
-- 以下是需要修复的数据（基于乱码特征识别）

-- =====================================================
-- 修复 dafuweng_sales.customer 表
-- =====================================================

-- 先查看所有需要修复的数据
SELECT '=== 待修复的客户数据 ===' as info;
SELECT id, name, HEX(name) as hex_name 
FROM dafuweng_sales.customer 
WHERE name IS NOT NULL 
  AND name != '' 
  AND (HEX(name) LIKE '%C3%' OR HEX(name) LIKE '%C2%')
ORDER BY id;

-- 备份表
CREATE TABLE IF NOT EXISTS dafuweng_sales.customer_backup_20260422 AS 
SELECT * FROM dafuweng_sales.customer;

